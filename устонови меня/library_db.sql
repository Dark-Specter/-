SELECT 
    'Всего записей в таблице' as description,
    COUNT(*) as count
FROM books
UNION ALL
SELECT 
    'Уникальных авторов' as description,
    COUNT(DISTINCT author) as count
FROM books
UNION ALL
SELECT 
    'Уникальных стеллажей' as description,
    COUNT(DISTINCT rack) as count
FROM books
UNION ALL
SELECT 
    'Уникальных жанров' as description,
    COUNT(DISTINCT genre) as count
FROM books;

