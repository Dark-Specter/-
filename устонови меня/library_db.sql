-- 4. СОЗДАНИЕ ТАБЛИЦЫ КНИГ
CREATE TABLE IF NOT EXISTS books (
    id INT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    author VARCHAR(255) NOT NULL,
    year INT NOT NULL,
    genre VARCHAR(100),
    publisher VARCHAR(255),
    isbn VARCHAR(20),
    quantity INT DEFAULT 1,
    rack VARCHAR(10) DEFAULT '',
    shelf VARCHAR(10) DEFAULT '',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    -- Проверки для целостности данных
    CONSTRAINT chk_year CHECK (year >= 1000 AND year <= 2100),
    CONSTRAINT chk_quantity CHECK (quantity >= 0),
    
    -- Уникальность комбинации основных полей
    UNIQUE KEY unique_book (title, author, year)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

