-- 8. СТАТИСТИКА
SELECT '=== СТАТИСТИКА ===' AS info;
SELECT 
    COUNT(*) as total_books,
    COUNT(DISTINCT author) as unique_authors,
    COUNT(DISTINCT rack) as unique_racks,
    GROUP_CONCAT(DISTINCT rack ORDER BY rack) as all_racks
FROM books;

-- 9. ПРОВЕРКА КОЛИЧЕСТВА ЗАПИСЕЙ
